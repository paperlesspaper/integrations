function normalizeTargetLanguage(value) {
  const key = String(value || "").trim().toLowerCase();
  return aliases.get(key) || "French";
}

function normalizeTranslationLanguage(value) {
  const key = String(value || "").trim().toLowerCase();
  return key === "de" ? "de" : "en";
}

function normalizeDayKey(value) {
  const raw = String(value || "").trim();
  if (/^\d{4}-\d{2}-\d{2}$/.test(raw)) {
    return raw;
  }
  return new Date().toISOString().slice(0, 10);
}

function hashString(value) {
  let hash = 2166136261;
  for (let index = 0; index < value.length; index += 1) {
    hash ^= value.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function pickEntry(entries, dayKey, languageName, seed) {
  const hash = hashString(`${dayKey}|${languageName}|${seed || ""}`);
  return {
    entry: entries[hash % entries.length],
    index: hash % entries.length,
  };
}

function practicePrompt(word, translationLanguage) {
  if (translationLanguage === "de") {
    return `Schreib einen kurzen Satz mit "${word}" und lies ihn laut.`;
  }
  return `Write one short sentence with "${word}" and say it out loud.`;
}

export default async function handler({ query }) {
  const targetLanguage = normalizeTargetLanguage(query.targetLanguage || query.language);
  const translationLanguage = normalizeTranslationLanguage(query.translationLanguage);
  const dayKey = normalizeDayKey(query.date);
  const seed = String(query.seed || "");
  const language = targetLanguages[targetLanguage];
  const { entry, index } = pickEntry(language.entries, dayKey, targetLanguage, seed);
  const partOfSpeech =
    partLabels[translationLanguage]?.[entry.partOfSpeech] || entry.partOfSpeech;

  return {
    dayKey,
    index,
    count: language.entries.length,
    targetLanguage,
    targetLanguageCode: language.code,
    targetLanguageNativeName: language.nativeName,
    translationLanguage,
    word: entry.word,
    pronunciation: entry.pronunciation,
    partOfSpeech,
    translation: entry.translations[translationLanguage] || entry.translations.en,
    example: entry.example,
    exampleTranslation:
      entry.exampleTranslations[translationLanguage] || entry.exampleTranslations.en,
    note: entry.notes[translationLanguage] || entry.notes.en,
    practice: practicePrompt(entry.word, translationLanguage),
    level: entry.level,
    theme: entry.theme,
    updatedAt: new Date().toISOString(),
  };
}
